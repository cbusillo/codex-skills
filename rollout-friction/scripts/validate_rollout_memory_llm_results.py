#!/usr/bin/env python3
# /// script
# requires-python = ">=3.12"
# dependencies = []
# ///
"""Validate trusted-local LLM review outputs for rollout memory batches."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from collections import Counter
from typing import Any


NOTE_KEYS = [
    "people_updates",
    "profile_notes",
    "rollout_friction_notes",
    "local_llm_notes",
    "repo_specific_notes",
]
REQUIRED_KEYS = ["decisions", *NOTE_KEYS, "discard_reasons", "reviewed_candidate_ids"]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Validate local rollout-memory LLM result JSON.")
    parser.add_argument("prompt", type=Path, help="Prompt batch JSON generated by extract_rollout_memory.py.")
    parser.add_argument("result", type=Path, help="Result JSON from local-llm/scripts/lm_studio_chat.py --json.")
    parser.add_argument(
        "--allow-implicit-discards",
        action="store_true",
        help=(
            "Allow long-context review outputs to omit low-value candidates from decisions/discards. "
            "Every candidate must still appear in reviewed_candidate_ids; omitted candidates are counted as implicit discards."
        ),
    )
    parser.add_argument("--json", action="store_true", help="Emit validation summary as JSON.")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    try:
        summary = validate(args.prompt, args.result, allow_implicit_discards=args.allow_implicit_discards)
    except ValidationError as exc:
        summary = {"ok": False, "error": str(exc)}
    if args.json:
        print(json.dumps(summary, indent=2, sort_keys=True))
    elif summary["ok"]:
        print(
            "ok validate-rollout-memory-llm-result "
            f"reviewed={summary['reviewed_count']}/{summary['candidate_count']} "
            f"notes={summary['note_count']} discards={summary['discard_count']}"
        )
    else:
        print(f"error: {summary['error']}", file=sys.stderr)
    return 0 if summary["ok"] else 1


class ValidationError(RuntimeError):
    pass


def validate(prompt_path: Path, result_path: Path, allow_implicit_discards: bool = False) -> dict[str, Any]:
    prompt = read_json(prompt_path)
    result_envelope = read_json(result_path)
    content = result_envelope.get("content")
    if not isinstance(content, str) or not content.strip():
        raise ValidationError("result envelope does not contain string content")
    try:
        payload = json.loads(content)
    except json.JSONDecodeError as exc:
        raise ValidationError(f"model content is not strict JSON: {exc}") from exc
    if not isinstance(payload, dict):
        raise ValidationError("model content JSON must be an object")

    missing_keys = [key for key in REQUIRED_KEYS if key not in payload]
    if missing_keys:
        raise ValidationError(f"model content missing keys: {', '.join(missing_keys)}")
    for key in REQUIRED_KEYS:
        if not isinstance(payload[key], list):
            raise ValidationError(f"{key} must be a list")

    expected_ids = candidate_ids(prompt)
    decision_counts = ids_from_decisions(payload["decisions"])
    reviewed_ids = string_set(payload["reviewed_candidate_ids"])
    reviewed_counts = string_counts(payload["reviewed_candidate_ids"])
    discard_counts = ids_from_discards(payload["discard_reasons"])
    note_counts = ids_from_notes(payload)
    discard_ids = set(discard_counts)
    note_ids = set(note_counts)
    disposition_ids = discard_ids | note_ids
    covered_ids = reviewed_ids & disposition_ids
    missing_reviewed_ids = expected_ids - reviewed_ids
    missing_disposition_ids = expected_ids - disposition_ids
    missing_ids = sorted(expected_ids - covered_ids)
    explicit_decision_ids = set(decision_counts)
    unknown_ids = sorted((reviewed_ids | disposition_ids | explicit_decision_ids) - expected_ids)
    duplicate_reviewed_ids = sorted(id_ for id_, count in reviewed_counts.items() if count > 1)
    duplicate_disposition_ids = sorted(
        id_
        for id_, count in (note_counts + discard_counts).items()
        if count > 1 and id_ in expected_ids
    )
    overlapping_disposition_ids = sorted((note_ids & discard_ids) & expected_ids)
    missing_decision_ids = expected_ids - set(decision_counts)
    duplicate_decision_ids = sorted(id_ for id_, count in decision_counts.items() if count > 1 and id_ in expected_ids)
    strict_ok = (
        not missing_reviewed_ids
        and not missing_disposition_ids
        and not unknown_ids
        and not duplicate_reviewed_ids
        and not duplicate_disposition_ids
        and not overlapping_disposition_ids
        and not missing_decision_ids
        and not duplicate_decision_ids
    )
    implicit_discard_ids = missing_disposition_ids if allow_implicit_discards else set()
    selection_ok = (
        allow_implicit_discards
        and not missing_reviewed_ids
        and not unknown_ids
        and not duplicate_reviewed_ids
        and not duplicate_disposition_ids
        and not overlapping_disposition_ids
        and not duplicate_decision_ids
        and explicit_decision_ids == disposition_ids
        and explicit_decision_ids.issubset(expected_ids)
    )
    ok = strict_ok or selection_ok
    return {
        "ok": ok,
        "validation_mode": "implicit_discards" if allow_implicit_discards else "strict",
        "candidate_count": len(expected_ids),
        "reviewed_count": len(reviewed_ids),
        "covered_count": len(covered_ids),
        "implicit_discard_count": len(implicit_discard_ids),
        "missing_count": len(missing_ids),
        "missing_reviewed_count": len(missing_reviewed_ids),
        "missing_disposition_count": len(missing_disposition_ids),
        "unknown_count": len(unknown_ids),
        "duplicate_reviewed_count": len(duplicate_reviewed_ids),
        "duplicate_disposition_count": len(duplicate_disposition_ids),
        "overlapping_disposition_count": len(overlapping_disposition_ids),
        "decision_count": sum(decision_counts.values()),
        "missing_decision_count": len(missing_decision_ids),
        "duplicate_decision_count": len(duplicate_decision_ids),
        "missing_candidate_ids": missing_ids[:20],
        "implicit_discard_candidate_ids": sorted(implicit_discard_ids)[:20],
        "missing_reviewed_candidate_ids": sorted(missing_reviewed_ids)[:20],
        "missing_disposition_candidate_ids": sorted(missing_disposition_ids)[:20],
        "unknown_candidate_ids": unknown_ids[:20],
        "duplicate_reviewed_candidate_ids": duplicate_reviewed_ids[:20],
        "duplicate_disposition_candidate_ids": duplicate_disposition_ids[:20],
        "overlapping_disposition_candidate_ids": overlapping_disposition_ids[:20],
        "missing_decision_candidate_ids": sorted(missing_decision_ids)[:20],
        "duplicate_decision_candidate_ids": duplicate_decision_ids[:20],
        "note_count": sum(len(payload[key]) for key in NOTE_KEYS),
        "discard_count": len(payload["discard_reasons"]),
        "notes_by_key": {key: len(payload[key]) for key in NOTE_KEYS},
    }


def read_json(path: Path) -> dict[str, Any]:
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError) as exc:
        raise ValidationError(f"unable to read JSON from {path}: {exc}") from exc
    if not isinstance(payload, dict):
        raise ValidationError(f"{path} must contain a JSON object")
    return payload


def candidate_ids(prompt: dict[str, Any]) -> set[str]:
    candidates = prompt.get("candidates")
    if not isinstance(candidates, list):
        raise ValidationError("prompt does not contain a candidates list")
    ids: set[str] = set()
    for candidate in candidates:
        if not isinstance(candidate, dict) or not isinstance(candidate.get("candidate_id"), str):
            raise ValidationError("every prompt candidate must have a candidate_id")
        ids.add(candidate["candidate_id"])
    return ids


def string_set(values: list[Any]) -> set[str]:
    return {value for value in values if isinstance(value, str) and value.strip()}


def string_counts(values: list[Any]) -> Counter[str]:
    return Counter(value for value in values if isinstance(value, str) and value.strip())


def ids_from_discards(discards: list[Any]) -> Counter[str]:
    ids: Counter[str] = Counter()
    for item in discards:
        if isinstance(item, dict) and isinstance(item.get("candidate_id"), str):
            ids[item["candidate_id"]] += 1
    return ids


def ids_from_notes(payload: dict[str, Any]) -> Counter[str]:
    ids: Counter[str] = Counter()
    for key in NOTE_KEYS:
        for item in payload[key]:
            if isinstance(item, dict):
                value = item.get("candidate_id") or item.get("source_candidate_id")
                if isinstance(value, str):
                    ids[value] += 1
                elif isinstance(value, list):
                    ids.update(item for item in value if isinstance(item, str))
    return ids


def ids_from_decisions(decisions: Any) -> Counter[str]:
    ids: Counter[str] = Counter()
    if not isinstance(decisions, list):
        return ids
    for item in decisions:
        if isinstance(item, dict) and isinstance(item.get("candidate_id"), str):
            ids[item["candidate_id"]] += 1
    return ids


if __name__ == "__main__":
    raise SystemExit(main())
